#include <iostream>

using namespace std;

int main()
{
    int month;
    cout << "Enter a Month Number (1-12) ";
    cin >> month;

    if (month = 1)
        cout << "Jan";
    else if (month = 2)
        cout << "Feb";
    else if (month = 3)
        cout << "March";
    else if (month = 4)
        cout << "April";
    else if (month = 5)
        cout << "May";
    else if (month = 6)
        cout << "June";
    else if (month = 7)
        cout << "Jul";
    else if (month = 8)
        cout << "Aug";
    else if (month = 9)
        cout << "Sept";
    else if (month = 10)
        cout << "Oct";
    else if (month = 11)
        cout << "Nov";
    else if (month = 12)
        cout << "Dec";
    else
        cout << "invalid Date";
}
